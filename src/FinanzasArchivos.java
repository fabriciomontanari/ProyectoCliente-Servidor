    public void agregar(){
        Finanzas f=new Finanzas();
        f.setDia(jTextField1.getText());
        f.setEntradas(jTextField2.getText());
        f.setComidas(jTextField3.getText());
        f.setPropinas(jTextField4.getText());

        try{
        DataOutputStream salida=new DataOutputStream(new FileOutputStream("datosFinanzas.dat",true));
        salida.writeUTF(f.getDia());
        salida.writeUTF(f.getEntradas());
        salida.writeUTF(f.getEntradas());
        salida.writeUTF(f.getPropinas());
 
        JOptionPane.showMessageDialog(null,"Datos guardados correctamente!");
        limpiarControles();
        salida.close();
    }catch(IOException e){
        JOptionPane.showMessageDialog(null,"Error al guardar!");
    }
        
    }
    
    public void consultar(){
        boolean proximo=true;
     Finanzas f=new Finanzas();
     try{
         DataInputStream entrada=new DataInputStream(new FileInputStream("datosFinanzas.dat"));
         try{
             while(true){
            f.setDia(entrada.readUTF());
            f.setEntradas(entrada.readUTF());
            f.setComidas(entrada.readUTF());
            f.setPropinas(entrada.readUTF());
 
            if(jTextField1.getText().equals(f.getDia())){
               jTextField2.setText(f.getEntradas());
               jTextField3.setText(f.getComidas());
               jTextField4.setText(f.getPropinas());

               proximo=false;
            }
         }
     }catch(EOFException eeof){
         entrada.close();
         if(proximo){
             JOptionPane.showMessageDialog(null,"Dia no encontrado!");
         }
     }
 }catch(FileNotFoundException fnfe){
   JOptionPane.showMessageDialog(null,"No se encontro el archivo!");
 }catch(IOException eioe){
     JOptionPane.showMessageDialog(null,"Â¡Error!");
 } 
        
    }
    public void limpiarControles() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }

