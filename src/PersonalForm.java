    public void agregar(){
        Personal p=new Personal();
        p.setNombre(jTextField1.getText());
        p.setPrimerApell(jTextField2.getText());
        p.setSegundoApell(jTextField3.getText());
        p.setNacionalidad(jTextField4.getText());
        p.setEdad(jTextField5.getText());
        p.setCorreo(jTextField6.getText());
        p.setNumEmpleado(jTextField7.getText());
        try{
        DataOutputStream salida=new DataOutputStream(new FileOutputStream("datosPersonaal.dat",true));
        salida.writeUTF(p.getNombre());
        salida.writeUTF(p.getPrimerApell());
        salida.writeUTF(p.getSegundoApell());
        salida.writeUTF(p.getNacionalidad());
        salida.writeUTF(p.getEdad());
        salida.writeUTF(p.getCorreo());
        salida.writeUTF(p.getNumEmpleado());
        
        JOptionPane.showMessageDialog(null,"Datos guardados correctamente!");
        limpiarControles();
        salida.close();
    }catch(IOException e){
        JOptionPane.showMessageDialog(null,"Error al guardar!");
    }
        
    }
    public void consultar(){
        boolean proximo=true;
     Personal p=new Personal();
     try{
         DataInputStream entrada=new DataInputStream(new FileInputStream("datosPersonaal.dat"));
         try{
             while(true){
            p.setNombre(entrada.readUTF());
            p.setPrimerApell(entrada.readUTF());
            p.setSegundoApell(entrada.readUTF());
            p.setNacionalidad(entrada.readUTF());
            p.setEdad(entrada.readUTF());
            p.setCorreo(entrada.readUTF());
            p.setNumEmpleado(entrada.readUTF());
            
            
            if(jTextField7.getText().equals(p.getNumEmpleado())){
               jTextField2.setText(p.getPrimerApell());
               jTextField3.setText(p.getSegundoApell());
               jTextField4.setText(p.getNacionalidad());
               jTextField5.setText(p.getEdad());
               jTextField6.setText(p.getCorreo());
               jTextField1.setText(p.getNombre());
               
               proximo=false;
            }
         }
     }catch(EOFException eeof){
         entrada.close();
         if(proximo){
             JOptionPane.showMessageDialog(null,"Persona no encontrada!");
         }
     }
 }catch(FileNotFoundException fnfe){
   JOptionPane.showMessageDialog(null,"No se encontro el archivo!");
 }catch(IOException eioe){
     JOptionPane.showMessageDialog(null,"Persona encontrada!");
 } 
        
    }
